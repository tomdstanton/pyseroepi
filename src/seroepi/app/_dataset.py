@module.server
def dataset_server(input, output, session, app_state: dict):
    shared_df = app_state["shared_df"]
    pw_collections_cache = app_state["pw_collections_cache"]
    shared_dist = app_state["shared_dist"]
    shared_trans_dist = app_state.setdefault("shared_trans_dist", reactive.Value(None))
    dataset_registry = app_state.setdefault("dataset_registry", reactive.Value({}))

    @reactive.Effect
    def manage_accordion_state():
        if shared_df.get() is None:
            ui.update_accordion("dataset_accordion", show="Load Data 💽")

    @render.ui
    def dynamic_meta_mapping():
        if not (meta_info := input.metadata_file()): return ui.div()
        try:
            mapper = ColMapper(cols := pd.read_csv(Path(meta_info[0]["datapath"]), nrows=0).columns.tolist())
            return ui.accordion(
                ui.accordion_panel(
                    "Map Metadata Columns",
                    ui.p("Please match your columns to the required fields:", class_="text-muted small"),
                    ui.input_selectize("map_id", "Sample ID", choices=[""] + cols, selected=mapper.guess("map_id")),
                    ui.layout_column_wrap(
                        ui.input_selectize("map_date", "Temporal", choices=[""] + cols, selected=mapper.guess("map_date")),
                        ui.input_select("map_date_res", "Resolution", choices=TemporalResolution.ui_labels(), selected=TemporalResolution.UNKNOWN.value),
                        width=1 / 2
                    ),
                    ui.layout_column_wrap(
                        ui.input_selectize("map_spatial", "Spatial", choices=[""] + cols, selected=mapper.guess("map_spatial")),
                        ui.input_select("map_spatial_res", "Resolution", choices=SpatialResolution.ui_labels(), selected=SpatialResolution.UNKNOWN.value),
                        width=1 / 2
                    ),
                    ui.input_selectize("map_lat", "Latitude", choices=[""] + cols, selected=mapper.guess("map_lat")),
                    ui.input_selectize("map_lon", "Longitude", choices=[""] + cols, selected=mapper.guess("map_lon"))
                ),
                id="meta_accordion", open="Map Metadata Columns"
            )
        except Exception as e:
            return ui.div(f"Could not read metadata columns: {e}", class_="text-danger small")

    @reactive.Effect
    @reactive.event(input.btn_process)
    async def load_data():
        if not (genotype_info := input.genotype_file()):
            ui.notification_show("Kleborate output is required.", type="warning")
            return

        async with ui_task("Pipeline Error") as p:
            try:
                p.set(message="Reading and Merging Datasets...", value=20)
                await sleep(0)
                metadata_kwargs, meta_path = {}, None
                if metadata_info := input.metadata_file():
                    meta_path = Path(metadata_info[0]["datapath"])
                    metadata_kwargs = {
                        "id_col": input.map_id() or None, "date_col": input.map_date() or None, "date_res": input.map_date_res() or None,
                        "spatial_col": input.map_spatial() or None, "spatial_res": input.map_spatial_res() or None,
                        "lat_col": input.map_lat() or None, "lon_col": input.map_lon() or None
                    }
                    if not metadata_kwargs["id_col"]:
                        ui.notification_show("You must select a Sample ID column to merge metadata.", type="error")
                        return

                ds_name = genotype_info[0]["name"]
                ParserClass = BaseGenotypeParser.get_parser(input.genotype_flavour())
                df = await to_thread(ParserClass.from_files, genotype_path=Path(genotype_info[0]["datapath"]), meta_path=meta_path, meta_kwargs=metadata_kwargs, dataset_name=ds_name)
                dist_obj = None
                if distance_info := input.distance_file():
                    p.set(message="Parsing Distances...", value=40)
                    await sleep(0)
                    dist_obj = await to_thread(GenomicDistances.from_file, Path(distance_info[0]["datapath"]), input.distance_flavour())

                p.set(message="Pipeline Complete!", value=100)
                await sleep(0)
                
                shared_df.set(df)
                shared_dist.set(dist_obj)
                shared_trans_dist.set(None)
                update_registry(dataset_registry, ds_name, {"df": df, "dist": dist_obj, "trans_dist": None})
                app_state["active_dataset_name"].set(ds_name)
                
                ui.notification_show("Data successfully loaded.", type="message", duration=4)
            except SchemaError as e:
                failed_col = getattr(e.schema, 'name', 'Unknown Column')
                ui.notification_show(f"Validation Error in column '{failed_col}'. See console for details.", type="error", duration=15)
                print(f"SCHEMA ERROR CASES:\n{e.failure_cases}")
                shared_df.set(None)
                ui.update_accordion("dataset_accordion", show="Load Data 💽")
            except SchemaErrors as e:
                ui.notification_show("Validation Error: Multiple schema violations detected.", type="error", duration=15)
                shared_df.set(None)
                ui.update_accordion("dataset_accordion", show="Data Ingestion")

    @reactive.Effect
    @reactive.event(input.btn_load_example)
    async def load_example_data():
        async with ui_task("Example Pipeline Error") as p:
            p.set(message="Loading example datasets...", value=20)
            await sleep(0)

            base_path = Path(__file__).parent / "data"
            genotype_path = base_path / "pathogenwatch-klepn-klebnet-neonatal-sepsis-kleborate.csv"
            meta_path = base_path / "pathogenwatch-klepn-klebnet-neonatal-sepsis-metadata.csv"
            dist_path = base_path / "pathogenwatch-klepn-klebnet-neonatal-sepsis-difference-matrix.csv"

            ds_name = "Neonatal Sepsis (Example)"
            metadata_kwargs = {
                "id_col": "NAME",
                "date_col": "COLLECTION DATE",
                "spatial_col": "COUNTRY",
                "lat_col": "LATITUDE",
                "lon_col": "LONGITUDE"
            }

            ParserClass = BaseGenotypeParser.get_parser("pathogenwatch-kleborate")
            df = await to_thread(ParserClass.from_files, genotype_path=genotype_path, meta_path=meta_path, meta_kwargs=metadata_kwargs, dataset_name=ds_name)
            
            p.set(message="Parsing Distances...", value=60)
            await sleep(0)
            dist_obj = await to_thread(GenomicDistances.from_file, dist_path, "pathogenwatch")

            p.set(message="Pipeline Complete!", value=100)
            await sleep(0)
            
            shared_df.set(df)
            shared_dist.set(dist_obj)
            shared_trans_dist.set(None)
            update_registry(dataset_registry, ds_name, {"df": df, "dist": dist_obj, "trans_dist": None})
            app_state["active_dataset_name"].set(ds_name)
            
            ui.notification_show("Example data successfully loaded.", type="message", duration=4)

    @reactive.Effect
    @reactive.event(input.btn_fetch_pw)
    async def fetch_pw_collections():
        api_key = input.pw_api_key()
        if not api_key:
            ui.notification_show("Please enter an API Key.", type="warning")
            return

        async with ui_task("API Error") as p:
            p.set(message="Connecting to Pathogenwatch...", value=30)
            await sleep(0)

            def fetch_collections():
                with PathogenwatchClient(api_key) as client:
                    return list(client.get_collections())

            collections = await to_thread(fetch_collections)

            if not collections:
                ui.notification_show("No collections found.", type="warning")
                return

            collections.sort(key=lambda c: (c.name or "").lower())
            cache_map = {c.uuid: c for c in collections}
            choices_map = {"": "Select..."}
            choices_map.update({c.uuid: c.name or "Unnamed Collection" for c in collections})

            pw_collections_cache.set(cache_map)
            ui.update_selectize("pw_collection", choices=choices_map)
            p.set(message="Collections fetched!", value=100)
            ui.notification_show(f"Found {len(collections)} collections.", type="message")

    @reactive.Effect
    @reactive.event(input.btn_load_pw)
    async def load_pw_collection():
        api_key, selected_uuid, cache = input.pw_api_key(), input.pw_collection(), pw_collections_cache.get()
        if not api_key or not selected_uuid or selected_uuid not in cache: return
        collection = cache[selected_uuid]

        async with ui_task("Load Error") as p:
                name = collection.name or "Collection"
                p.set(message=f"Fetching genomes for {name}...", value=30)
                await sleep(0)

                def fetch_genomes():
                    with PathogenwatchClient(api_key) as client:
                        return collection.get_genomes(client)

                genomes = await to_thread(fetch_genomes)

                if not genomes:
                    ui.notification_show("Collection is empty.", type="warning")
                    return

                p.set(message="Parsing to DataFrame...", value=80)
                await sleep(0)
                df = await to_thread(PathogenwatchGenomeParser.from_records, genomes, dataset_name=name)
                p.set(message="Pipeline Complete!", value=100)
                await sleep(0)
                shared_df.set(df)
                shared_dist.set(None)
                shared_trans_dist.set(None)
                update_registry(dataset_registry, name, {"df": df, "dist": None, "trans_dist": None})
                app_state["active_dataset_name"].set(name)
                ui.notification_show(f"Loaded {len(df)} genomes from Pathogenwatch.", type="message", duration=4)

    @reactive.Effect
    @reactive.event(input.btn_clear_data)
    def clear_data():
        for key, r_val in app_state.items():
            if key in ["pw_collections_cache", "results_registry", "formulation_registry", "dataset_registry"]: r_val.set({})
            else: r_val.set(None)
        ui.notification_show("All data and results cleared.", type="message")
        ui.update_accordion("dataset_accordion", show="Load Data 💽")

    @reactive.Effect
    @reactive.event(input.btn_calc_snp_clusters)
    async def calc_snp_clusters():
        df, dist = shared_df.get(), shared_dist.get()
        if df is None or dist is None: return
        async with ui_task("SNP Clustering Error") as p:
                p.set(message="Calculating SNP clusters...", value=30)
                await sleep(0)
                clusters = await to_thread(dist.get_clusters, threshold=input.snp_threshold())
                clusters.name = f"{Domain.CLUSTER.value}_snp_{input.snp_threshold()}"
                p.set(message="Merging to dataset...", value=70)
                await sleep(0)
                if clusters.name in df.columns: df = df.drop(columns=[clusters.name])
                df = df.join(clusters, on='sample_id')
                shared_df.set(df)
                
                ds_name = app_state["active_dataset_name"].get()
                if ds_name in dataset_registry.get():
                    updated_data = {**dataset_registry.get()[ds_name], "df": df}
                    update_registry(dataset_registry, ds_name, updated_data)
                p.set(message="Done!", value=100)
                ui.notification_show(f"Successfully generated {clusters.name}", type="message")

    @reactive.Effect
    @reactive.event(input.btn_calc_trans_clusters)
    async def calc_trans_clusters():
        df = shared_df.get()
        if df is None or not input.trans_clone_col(): return
        async with ui_task("Transmission Clustering Error") as p:
                p.set(message="Calculating transmission clusters...", value=40)
                await sleep(0)
                if not (df.epi.has_spatial and df.epi.has_temporal):
                    ui.notification_show("Requires both spatial and temporal metadata.", type="error")
                    return
                def run_trans_clusters():
                    net = df.epi.transmission_network(clone_col=input.trans_clone_col(),
                                                      spatial_threshold_km=input.trans_spatial_thr(),
                                                      temporal_threshold_days=input.trans_temporal_thr())
                    labels = df.epi.transmission_clusters(clone_col=input.trans_clone_col(),
                                                          spatial_threshold_km=input.trans_spatial_thr(),
                                                          temporal_threshold_days=input.trans_temporal_thr(),
                                                          network=net)
                    return net, labels
                t_net, t_clusters = await to_thread(run_trans_clusters)
                shared_trans_dist.set(t_net)
                if t_clusters.name in df.columns: df = df.drop(columns=[t_clusters.name])
                df = df.assign(**{t_clusters.name: t_clusters})
                shared_df.set(df)
                
                ds_name = app_state["active_dataset_name"].get()
                if ds_name in dataset_registry.get():
                    updated_data = {**dataset_registry.get()[ds_name], "df": df, "trans_dist": t_net}
                    update_registry(dataset_registry, ds_name, updated_data)
                p.set(message="Done!", value=100)
                ui.notification_show(f"Successfully generated {t_clusters.name}", type="message")

    @reactive.Effect
    def update_network_dropdowns():
        if (df := shared_df.get()) is not None:
            color_cols = list(dict.fromkeys(df.epi.stratify_cols + df.epi.cluster_cols + df.epi.genotypes))
            color_choices = build_grouped_choices(color_cols, "Other Variables", include_empty=True)
            ui.update_selectize("network_color_col", choices=color_choices, selected="")

    @reactive.Effect
    def update_trans_clone_dropdown():
        if (df := shared_df.get()) is not None:
            trait_choices = build_grouped_choices(df.epi.genotypes, "Other Traits", include_empty=True)
            st_col = f"{Domain.GENOTYPE.value}_ST"
            st_select = st_col if st_col in df.epi.genotypes else ""
            ui.update_selectize("trans_clone_col", choices=trait_choices, selected=st_select)

    @render.ui
    def dashboard_content():
        if (df := shared_df.get()) is None:
            return ui.div("Please upload and process files to view the global dataset.", class_="text-center mt-5 text-muted fs-4")
        return dt_download_ui("dashboard", "Global Dataset Preview")

    dt_download_server("dashboard", data_callable=lambda: shared_df.get(), filename="global_dataset.csv")

    @render.ui
    def network_plot_content():
        dist = shared_dist.get()
        trans_dist = shared_trans_dist.get()
        if dist is None and trans_dist is None:
            return ui.div("Calculate genomic or transmission clusters to view the network.", class_="text-center mt-5 text-muted fs-4")
        return output_widget("cluster_network_plot")

    @reactive.Calc
    async def network_layout():
        dist = shared_dist.get()
        if dist is not None: layout_net = dist
        else:
            trans_dist = shared_trans_dist.get()
            if trans_dist is not None: layout_net = trans_dist
            else: return None
        async with ui_task("Layout Error") as p:
            p.set(message="Preparing distance matrix...", value=20)
            await sleep(0)
            p.set(message="Calculating network layout (MDS)...", value=50)
            await sleep(0)
            pos = await to_thread(layout_net.layout)
            p.set(message="Layout complete!", value=100)
            await sleep(0)
            return pos

    @render_widget
    async def cluster_network_plot():
        df, dist, trans_dist = shared_df.get(), shared_dist.get(), shared_trans_dist.get()
        empty_theme = dict(plot_bgcolor='rgba(0,0,0,0)', paper_bgcolor='rgba(0,0,0,0)', font=dict(color='#94A3B8'))
        if df is None: return Figure().update_layout(title="Upload a dataset to view the network.", **empty_theme)
        if dist is not None: primary_net = dist
        elif trans_dist is not None: primary_net = trans_dist
        else: return Figure().update_layout(title="Upload a distance matrix or calculate transmission clusters.", **empty_theme)
        pos = await network_layout()
        if pos is None: return Figure().update_layout(title="Processing distance matrix...", **empty_theme)
        return render_plot(primary_net, PlotType.NETWORK, df=df, pos=pos, edge_type=input.network_edge_type(),
                           threshold=input.network_snp_threshold(), color_col=input.network_color_col(), trans_network=trans_dist)